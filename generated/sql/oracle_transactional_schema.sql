-- ============================================================
-- SECTION 5: DOCUMENT MASTER (root entity)
-- ============================================================

CREATE TABLE NOTARIST.DOC_MASTER (
    DOC_ID              VARCHAR2(36)            NOT NULL,
    DOC_CODE            VARCHAR2(100),
    DOC_TITLE           VARCHAR2(500)           NOT NULL,
    JENIS_DOKUMEN       VARCHAR2(30)            NOT NULL,
    KLASIFIKASI         VARCHAR2(30)            DEFAULT 'INTERNAL'  NOT NULL,
    STATUS_DOKUMEN      VARCHAR2(30)            DEFAULT 'PENDING_OCR' NOT NULL,
    ORIGINAL_FILENAME   VARCHAR2(500)           NOT NULL,
    FILE_SIZE_BYTES     NUMBER(15),
    FILE_MIME_TYPE      VARCHAR2(100),
    STORAGE_PATH        VARCHAR2(1000),
    STORAGE_BUCKET      VARCHAR2(200),
    PAGE_COUNT          NUMBER(5),
    CHECKSUM_SHA256     VARCHAR2(64),
    UPLOAD_BY           VARCHAR2(36),
    UPLOAD_DATE         DATE                    NOT NULL,
    IS_ACTIVE           NUMBER(1)               DEFAULT 1           NOT NULL,
    CREATED_AT          TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP NOT NULL,
    UPDATED_AT          TIMESTAMP WITH TIME ZONE,
    CREATED_BY          VARCHAR2(100),
    UPDATED_BY          VARCHAR2(100),
    CONSTRAINT PK_DOC_MASTER            PRIMARY KEY (DOC_ID),
    CONSTRAINT UQ_DOC_CODE              UNIQUE (DOC_CODE),
    CONSTRAINT FK_DOC_UPLOAD_BY         FOREIGN KEY (UPLOAD_BY)
                                        REFERENCES NOTARIST.USER_MASTER (USER_ID),
    CONSTRAINT CHK_DOC_JENIS            CHECK (JENIS_DOKUMEN IN (
                                            'AKTA', 'SOP', 'REGULASI', 'PERJANJIAN',
                                            'SERTIFIKAT_SCAN', 'SURAT', 'LAIN'
                                        )),
    CONSTRAINT CHK_DOC_KLASIFIKASI      CHECK (KLASIFIKASI IN (
                                            'PUBLIC', 'INTERNAL',
                                            'CONFIDENTIAL', 'STRICTLY_CONFIDENTIAL'
                                        )),
    CONSTRAINT CHK_DOC_STATUS           CHECK (STATUS_DOKUMEN IN (
                                            'PENDING_OCR', 'PENDING_EXTRACTION',
                                            'PENDING_EMBEDDING', 'ACTIVE',
                                            'SUPERSEDED', 'ARCHIVED', 'DELETED'
                                        )),
    CONSTRAINT CHK_DOC_IS_ACTIVE        CHECK (IS_ACTIVE IN (0, 1))
);

COMMENT ON TABLE  NOTARIST.DOC_MASTER                   IS 'Root entity — setiap dokumen yang masuk sistem';
COMMENT ON COLUMN NOTARIST.DOC_MASTER.DOC_CODE          IS 'Business key yang readable, contoh: AKTA-2026-00123';
COMMENT ON COLUMN NOTARIST.DOC_MASTER.STATUS_DOKUMEN    IS 'Status dalam pipeline: PENDING_OCR → ACTIVE';
COMMENT ON COLUMN NOTARIST.DOC_MASTER.KLASIFIKASI       IS 'Tingkat kerahasiaan dokumen';
COMMENT ON COLUMN NOTARIST.DOC_MASTER.CHECKSUM_SHA256   IS 'Fingerprint file untuk deteksi duplikat';

CREATE INDEX IDX_DOC_JENIS          ON NOTARIST.DOC_MASTER (JENIS_DOKUMEN);
CREATE INDEX IDX_DOC_STATUS         ON NOTARIST.DOC_MASTER (STATUS_DOKUMEN);
CREATE INDEX IDX_DOC_KLASIFIKASI    ON NOTARIST.DOC_MASTER (KLASIFIKASI);
CREATE INDEX IDX_DOC_UPLOAD_DATE    ON NOTARIST.DOC_MASTER (UPLOAD_DATE);
CREATE INDEX IDX_DOC_ACTIVE         ON NOTARIST.DOC_MASTER (IS_ACTIVE);


