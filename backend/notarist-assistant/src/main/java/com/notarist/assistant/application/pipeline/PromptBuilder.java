package com.notarist.assistant.application.pipeline;

import com.notarist.assistant.domain.model.AssembledPrompt;
import com.notarist.assistant.domain.model.PromptVersion;
import org.springframework.stereotype.Service;

import java.util.List;
import java.util.UUID;

/**
 * Builds the versioned prompt that will be sent to the LLM.
 *
 * Assembly order (mandatory):
 *   1. System prompt = PromptVersion template + assembled context (chunks with [Sumber: X] markers)
 *   2. User prompt   = question formatted per template
 *   3. Token count estimated for budget tracking
 *
 * Citations are already embedded in assembledContext — not generated by the LLM.
 */
@Service
public class PromptBuilder {

    private static final double TOKEN_RATIO = 1.3;

    public AssembledPrompt build(
            String userQuery,
            String assembledContext,
            PromptVersion version,
            UUID retrievalSnapshotId,
            List<String> chunkIds) {

        String systemPrompt = version.buildSystemPrompt(assembledContext);
        String userPrompt   = version.buildUserPrompt(userQuery);

        int systemTokens = estimateTokens(systemPrompt);
        int userTokens   = estimateTokens(userPrompt);

        return new AssembledPrompt(
                systemPrompt,
                userPrompt,
                version,
                retrievalSnapshotId,
                systemTokens,
                userTokens,
                systemTokens + userTokens,
                List.copyOf(chunkIds));
    }

    private int estimateTokens(String text) {
        if (text == null || text.isBlank()) return 0;
        return (int) Math.ceil(text.length() / TOKEN_RATIO);
    }
}
