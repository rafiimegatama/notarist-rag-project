COMMENT ON COLUMN NOTARIST.DOC_TAG_MAP.CONFIDENCE_SCORE IS 'Hanya diisi jika TAG_SOURCE = AI, range 0.0000–1.0000';

CREATE INDEX IDX_TAGMAP_DOC     ON NOTARIST.DOC_TAG_MAP (DOC_ID);
CREATE INDEX IDX_TAGMAP_TAG     ON NOTARIST.DOC_TAG_MAP (TAG_ID);


