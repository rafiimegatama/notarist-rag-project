-- ============================================================
-- SECTION 1: USER & ACCESS
-- ============================================================

CREATE TABLE NOTARIST.USER_MASTER (
    USER_ID             VARCHAR2(36)            NOT NULL,
    USERNAME            VARCHAR2(100)           NOT NULL,
    FULL_NAME           VARCHAR2(200)           NOT NULL,
    EMAIL               VARCHAR2(200)           NOT NULL,
    ROLE                VARCHAR2(30)            NOT NULL,
    STATUS              VARCHAR2(20)            DEFAULT 'ACTIVE'    NOT NULL,
    LAST_LOGIN_AT       TIMESTAMP WITH TIME ZONE,
    CREATED_AT          TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP NOT NULL,
    UPDATED_AT          TIMESTAMP WITH TIME ZONE,
    CREATED_BY          VARCHAR2(100),
    UPDATED_BY          VARCHAR2(100),
    CONSTRAINT PK_USER_MASTER       PRIMARY KEY (USER_ID),
    CONSTRAINT UQ_USER_USERNAME     UNIQUE (USERNAME),
    CONSTRAINT UQ_USER_EMAIL        UNIQUE (EMAIL),
    CONSTRAINT CHK_USER_ROLE        CHECK (ROLE IN (
                                        'STAFF', 'NOTARIS', 'PPAT_OFFICER',
                                        'PIMPINAN', 'ADMIN', 'READONLY'
                                    )),
    CONSTRAINT CHK_USER_STATUS      CHECK (STATUS IN ('ACTIVE', 'INACTIVE', 'SUSPENDED'))
);

COMMENT ON TABLE  NOTARIST.USER_MASTER              IS 'Master data pengguna internal sistem notaris';
COMMENT ON COLUMN NOTARIST.USER_MASTER.USER_ID      IS 'UUID primary key, generated by application';
COMMENT ON COLUMN NOTARIST.USER_MASTER.ROLE         IS 'STAFF|NOTARIS|PPAT_OFFICER|PIMPINAN|ADMIN|READONLY';
COMMENT ON COLUMN NOTARIST.USER_MASTER.STATUS       IS 'ACTIVE|INACTIVE|SUSPENDED';

CREATE INDEX IDX_USER_STATUS ON NOTARIST.USER_MASTER (STATUS);


